                <h2>History Of San Jose Del Monte Bulacan</h2>
                <p>The idea of reduccion was widely spread during the Spanish Colonial Period. This was one of the systems used to spread Catholicism to the remote areas of Bulacan. Reduccion or reduction of population was practiced in the largely populated areas particularly in Meycauayan, Bulacan and San Jose, Centro Da Baloges. In March 1750, a decree from the Archbishop of Manila on the creation of new municipalities was announced in Lagulo Church in Meycauayan. The decree included the list of families who volunteered to be relocated. Where previously the only occupants were Itas and Dumagats, San Jose del Monte (SJDM) became a municipality as a result of reduccion from Meycauayan. These families brought with them rice, wine, nganga and salt from Lagulo (now Malhacan) in exchange for the wild pigs, deer, yantok and almasigan of the Itas and Dumagats. Solares, including intended lots for main roads, were peacefully distributed to the new occupants after being measured and surveyed. The municipality of SJDM was founded on March 2, 1752. The population, not exceeding 200 people, belonged to the family of farmers and stonecutters of Libtong and Meycauayan. They lived a simple lifestyle and raised fish, root crops, vegetables, fruits and other natural products.</p>
                <p>Under the American regime in 1901, SJDM, being less progressive, weak and lacking in peace and order was placed under the political supervision of Sta. Maria, Bulacan. Under American dispensation in 1918, the town became an independent Municipality with Honorable Ciriaco Gallardo as the first Municipal Mayor. The Japanese Imperial Army took over the local government of San Jose del Monte from 1942-1943. In resistance, the municipality formed its own guerrilla unit. SJDM experienced many casualties when the Americans bombed the Poblacion on January 11, 1945 and again on January 14, 1945. The Municipal Building was burned by dissidents on October 10, 1950. The year 1961 marked the opening of the first Government Resettlement Project, the Sapang Palay Resettlement Area, covering 752 hectares. In January 25, 1978, nine barangays were created under P.D. 1921. With the passage of the new local government code in 1991 came the reformulation of equal wealth sharing between the national and local units and the realization of the residents of having their own barangays. This move led to the creation of an additional 41 barangays under Provincial Ordinance promulgated by RA 337 in December 1991. With the advent of the new millennium, SJDM’s population registered a staggering 315,807. It continues to grow as private subdivisions mushroom in strategic areas of the Municipality.</p>
                <p>On September 10, 2000, SJDM was proclaimed as a Component City under Republic Act No. 8797. It is said to be the largest town in the whole province of Bulacan in terms of land area and population. Also known as the “Balcony of the Metropolis”, it is the first City in the province of Bulacan and recorded as the 86th City of the Philippines. On December 18, 2003, the City of San Jose became the 1st Lone Congressional District in Bulacan.</p>
